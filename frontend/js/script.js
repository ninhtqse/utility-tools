function copyToClipboard(preElement, button) {
    let text = preElement.innerText;
    navigator.clipboard.writeText(text).then(() => {
        button.innerText = "‚úÖ";
        setTimeout(() => (button.innerText = "üìã"), 1500);
    }).catch(err => console.error("Copy failed:", err));
}
